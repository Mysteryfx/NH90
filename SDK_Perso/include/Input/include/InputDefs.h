// события от клавиатуры
#define KEYBOARD(in) (in)

// события от мыши
#define MOUSE(in) (KEYBOARD(in) + MAX_DEVICE_ACTION_COUNT)

// события от headtracker
#define HEADTRACKER(in) ((MOUSE(in) + MAX_DEVICE_ACTION_COUNT))

// события от trackir
#define TRACKIR(in) ((HEADTRACKER(in) + MAX_DEVICE_ACTION_COUNT))

// события от джойстика с номером n
